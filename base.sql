

CREATE TABLE Antecedente(
    AntecedenteID INT AUTO_INCREMENT PRIMARY KEY,
    NombreAutor VARCHAR(255), 
    Anio VARCHAR(255), 
    TipoAntecedente VARCHAR(255), 
    Titulo VARCHAR(255) , 
    ProblemaEstudiado VARCHAR(255), 
    Metodologia VARCHAR(255) , 
    ObjetivoPrincipal VARCHAR(255) ,
    Resultados VARCHAR(255) 
)
